<?php
    // Establish a database connection
    $db_host = "localhost";
    $db_name = "fyp";
    $db_user = "root";
    $db_pass = "";
    
    $conn = mysqli_connect($db_host, $db_user, $db_pass, $db_name);
    
    // Check connection
    if (mysqli_connect_errno()) {
        echo "Failed to connect to MySQL: " . mysqli_connect_error();
        exit();
    }
    
    // Check if the form has been submitted
    if (isset($_POST['save'])) {
        // Retrieve form data
        $product_name = mysqli_real_escape_string($conn, $_POST['product_name']);
        $product_price = mysqli_real_escape_string($conn, $_POST['product_price']);
        $product_description = mysqli_real_escape_string($conn, $_POST['product_description']);
        $product_count = mysqli_real_escape_string($conn, $_POST['product_count']);
        $product_type = mysqli_real_escape_string($conn, $_POST['product_type']);
        $product_keyword = mysqli_real_escape_string($conn, $_POST['product_keyword']);
        $product_image = mysqli_real_escape_string($conn, $_POST['product_image']);
        $product_size = mysqli_real_escape_string($conn, $_POST['product_size']);
        $product_color = mysqli_real_escape_string($conn, $_POST['product_color']);
        $product_gender = mysqli_real_escape_string($conn, $_POST['product_gender']);
    
        // Check if product already exists in database
        $check_query = "SELECT product_count FROM product WHERE product_name = '$product_name' AND product_size = '$product_size'";
        $result = mysqli_query($conn, $check_query);
        if (mysqli_num_rows($result) > 0) {
            $row = mysqli_fetch_assoc($result);
            $original_count = $row['product_count'];
            $new_count = intval($product_count) + intval($original_count);

            
            // Update existing product in database with new count
            $update_query = "UPDATE product SET product_price = '$product_price', product_description = '$product_description', product_count = '$new_count', product_type = '$product_type', product_keyword = '$product_keyword', product_image = '$product_image', product_color = '$product_color', product_gender = '$product_gender' WHERE product_name = '$product_name' AND product_size = '$product_size'";
            
            if (mysqli_query($conn, $update_query)) {
                echo "Product updated successfully.";
            } else {
                echo "Error updating product: " . mysqli_error($conn);
            }
        } else {
            // Insert new product into database
            $insert_query = "INSERT INTO product (product_name, product_price, product_description, product_count, product_type, product_keyword, product_image, product_size, product_color,product_gender)
                      VALUES ('$product_name', '$product_price', '$product_description', '$product_count', '$product_type', '$product_keyword', '$product_image', '$product_size', '$product_color','$product_gender')";
            
            if (mysqli_query($conn, $insert_query)) {
                echo "Product saved successfully.";
            } else {
                echo "Error saving product: " . mysqli_error($conn);
            }
        }
    }

    
        
    

    
    
    // Close database connection
    mysqli_close($conn);
?>
